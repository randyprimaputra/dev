void main(){
  // FOR LOOP: is definite function
  for (int i = 0; i < 4; i++){
    print("Hello");
  }

  // WHILE LOOP: is indefinite function
  int s = 0;
  while ( s <4) {
    print("Nani");
    s++;
  }

  // Do While LOOP: is indefinite function
  int g = 0;
  do {
    print("Hello again!!");
    g++;
  } while ( g < 4);

}